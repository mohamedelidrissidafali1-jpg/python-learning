# Comparison operators
print("=== Comparison Operators ===\n")

age = 20

# Comparisons return True or False (boolean)
print(f"age = {age}")
print(f"age > 18 is {age > 18}")
print(f"age < 18 is {age < 18}")
print(f"age == 20 is {age == 20}")  # == means "equal to"
print(f"age != 20 is {age != 20}")  # != means "not equal to"
print(f"age >= 20 is {age >= 20}")
print(f"age <= 19 is {age <= 19}")

# Try with different values
score = 85

print(f"\nscore = {score}")
print(f"score >= 90 is {score >= 90}")
print(f"score >= 80 is {score >= 80}")
print(f"score == 85 is {score == 85}")

# Comparing strings
name = "Ahmed"
print(f"\nname = '{name}'")
print(f"name == 'Ahmed' is {name == 'Ahmed'}")
print(f"name == 'ahmed' is {name == 'ahmed'}")  # Case matters!